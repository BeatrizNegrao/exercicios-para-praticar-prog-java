//1) Faça um programa que leia um vetor de 5 números inteiros e mostre-os.

public class Ex1 {
    public static void main(String[] args) {
        int vetor[] = { 1, 2, 3, 4, 5 };

        for (int i = 0; i < 5; i++) {
            System.out.println(vetor[i]);
        }

    }
}
